package main

import "fmt"

func math() {
	var result = 19 + 20
	fmt.Println(result)

	var a = 10
	var b = 20
	var c = a * b

	fmt.Println(c)

	//augmented assigments
	var i = 30
	i += 10
	fmt.Println(i)

	var d = 20
	d -= 10
	fmt.Println(d)

	var e = 10
	e *= 10
	fmt.Println(e)

	var f = 100
	f /= 10
	fmt.Println(f)

	var g = 20
	g %= 10
	fmt.Println(g)

	//unary

}
